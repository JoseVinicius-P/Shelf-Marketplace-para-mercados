package com.jv.shelf_cliente.modelos;

public class Endereco {
    private String informacao_adicional, lote, numero, quadra, rua_avenida;

    public Endereco() {
    }

    public String getInformacao_adicional() {
        return informacao_adicional;
    }

    public void setInformacao_adicional(String informacao_adicional) {
        this.informacao_adicional = informacao_adicional;
    }

    public String getLote() {
        return lote;
    }

    public void setLote(String lote) {
        this.lote = lote;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getQuadra() {
        return quadra;
    }

    public void setQuadra(String quadra) {
        this.quadra = quadra;
    }

    public String getRua_avenida() {
        return rua_avenida;
    }

    public void setRua_avenida(String rua_avenida) {
        this.rua_avenida = rua_avenida;
    }


}
